export function detectLocale(): Locale {
  if (typeof navigator === "undefined") return "de"
  const lang = navigator.language?.slice(0, 2).toLowerCase()
  if (lang === "de") return "de"
  if (lang === "es") return "es"
  if (lang === "it") return "it"
  return "en"
}

